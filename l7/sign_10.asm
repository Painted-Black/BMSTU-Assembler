PUBLIC S10

EXTRN  U10 : NEAR

CSEG	SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CSEG
		
S10	PROC NEAR
	PUSH BP
	MOV  BP, SP
		
	MOV  CX, [BP + 4]
	MOV  BX, [BP + 6]
		
	CMP  CL, 0
	JE   SKIP_NEG
		
	MOV  AH, 2
	MOV  DL, '-'
	INT  21H
		
	NEG  BX
		
SKIP_NEG:
	PUSH  BX
	PUSH  CX
		
	CALL  U10
		
	POP BP
	RET 4
S10	ENDP
CSEG	ENDS
END