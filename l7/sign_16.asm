PUBLIC S16

EXTRN  U16 : NEAR

CSEG	SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CSEG

		
S16	PROC NEAR
	PUSH BP
	MOV  BP, SP
		
	MOV  CX, [BP + 4]
	MOV  BX, [BP + 6]
		
	CMP  CL, 0
	JE   SKIP_NEG
		
	MOV  AH, 2
	MOV  DL, '-'
	INT  21H
		
	NEG  BX
		
SKIP_NEG:
	PUSH  BX
	PUSH  CX
		
	CALL  U16
		
	POP BP
	RET 4
S16	ENDP
	
CSEG	ENDS
END